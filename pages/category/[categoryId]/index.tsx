export const getStaticProps = async (context: any) => {
  console.log("conm",context)
  const id = context.params.categoryId

  try {
    const response = await fetch(
      `http://localhost:8000/api/category/${id}/get`,
      {
        method: "GET",
        headers: {
          Accept: "application/json",
          "Content-Type": "text/plain",
        },
      }
    )
    const category: Category = await response.json()
    return {
      props: {
        category: category,
      },
    }
  } catch (error: any) {
    return {
      props: { errCode: 500, message: error },
    }
  }
}

export const getStaticPaths = async () => {
  try {
    const response = await fetch(
      `http:/localhost:8000/api/category/get/0/1000`,
      {
        method: "GET",
        headers: {
          Accept: "application/json",
          "Content-Type": "text/plain",
        },
      }
    )

    const categories = await response.json()
    const ids = categories.map((category: any) => category.categoryId)
    const paths = ids.map((id: number) => ({
      params: {
        categoryId: id.toString(),
      },
    }))
    return {
      paths,
      fallback: false,
    }
  } catch (error: any) {
    return {
      props: { errCode: 500, message: error },
    }
  }
}

